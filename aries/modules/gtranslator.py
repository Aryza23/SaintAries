from gpytranslate import Translator
from telegram.ext import CommandHandler, CallbackContext
from telegram import (
    Message,
    Chat,
    User,
    ParseMode,
    Update,
    InlineKeyboardMarkup,
    InlineKeyboardButton,
)
from aries import dispatcher, pbot
from pyrogram import filters
from aries.modules.disable import DisableAbleCommandHandler


__help__ = """ 
Use this module to translate stuff!
üîò *Commands:*
‚ùç `/tl` (or `/tr`): as a reply to a message, translates it to English.
‚ùç `/tl <lang>`: translates to <lang>
üîò Ex: 
 ‚áù `/tl ja`: translates to Japanese.
‚ùç `/tl <source>//<dest>`: translates from <source> to <lang>.
üîò Ex: 
 ‚áù `/tl ja//en`: translates from Japanese to English.
‚ùç `/langs`: get a list of supported languages for translation.
"""

__mod_name__ = "üîò Translator"


trans = Translator()


@pbot.on_message(filters.command(["tl", "tr"]))
async def translate(_, message: Message) -> None:
    reply_msg = message.reply_to_message
    if not reply_msg:
        await message.reply_text("Reply to a message to translate it!")
        return
    if reply_msg.caption:
        to_translate = reply_msg.caption
    elif reply_msg.text:
        to_translate = reply_msg.text
    try:
        args = message.text.split()[1].lower()
        if "//" in args:
            source = args.split("//")[0]
            dest = args.split("//")[1]
        else:
            source = await trans.detect(to_translate)
            dest = args
    except IndexError:
        source = await trans.detect(to_translate)
        dest = "id"
    translation = await trans(to_translate, sourcelang=source, targetlang=dest)
    reply = (
        f"‚ùç <b>Translated from ‚áù {source} to {dest}</b>:\n"
        f"‚áù <code>{translation.text}</code>"
    )

    await message.reply_text(reply, parse_mode="html")


def languages(update: Update, context: CallbackContext) -> None:
    update.effective_message.reply_text(
        "Click on the button below to see the list of supported language codes.",
        reply_markup=InlineKeyboardMarkup(
            [
                [
                    InlineKeyboardButton(
                        text="Language codes",
                        url="https://telegra.ph/Lang-Codes-03-19-3",
                    ),
                ],
            ],
            disable_web_page_preview=True,
        ),
    )


LANG_HANDLER = DisableAbleCommandHandler("langs", languages, run_async=True)

dispatcher.add_handler(LANG_HANDLER)
